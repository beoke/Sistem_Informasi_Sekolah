CREATE TABLE SiswaWali (
	SiswaId INT NOT NULL DEFAULT(0),
	JenisWali INT NOT NULL DEFAULT(0),
	Nama VARCHAR(50) NOT NULL DEFAULT(''),
	TmpLahir VARCHAR(50) NOT NULL DEFAULT(''),
	TglLahir DATETIME NOT NULL DEFAULT('3000-01-01'),
	Agama VARCHAR(50) NOT NULL DEFAULT(''),
	Kewarga VARCHAR(50) NOT NULL DEFAULT('') ,
	Pendidikan VARCHAR(50) NOT NULL DEFAULT(''),
	Pekerjaan VARCHAR(50) NOT NULL DEFAULT(''),
	Penghasilan DECIMAL(18, 2) NOT NULL DEFAULT(0),
	Alamat VARCHAR(255) NOT NULL DEFAULT(''),
	NoKK VARCHAR(20) NOT NULL DEFAULT(''),
	NoTelp VARCHAR(20) NOT NULL DEFAULT(''),
	StatusHidup VARCHAR(50) NOT NULL DEFAULT(''),
	NIK VARCHAR(20) NOT NULL DEFAULT(''),
	TahunMeninggal VARCHAR(4) NOT NULL DEFAULT(''),
	)
GO

    CREATE CLUSTERED INDEX IX_SiswaWali_SiswaId
        ON SiswaWali (SiswaId, JenisWali)
GO
